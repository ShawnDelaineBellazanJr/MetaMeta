namespace MetaMeta.Orchestration.Models;

/// <summary>
/// Defines the styles of reasoning that can be applied.
/// </summary>
public enum ReasoningStyle
{
    /// <summary>
    /// Methodical, logical analysis breaking down problems into components.
    /// </summary>
    Analytical,
    
    /// <summary>
    /// Reasoning that explores novel connections and possibilities.
    /// </summary>
    Creative,
    
    /// <summary>
    /// Rigorous evaluation of arguments, evidence, and assumptions.
    /// </summary>
    Critical,
    
    /// <summary>
    /// Long-term, goal-oriented thinking with broader perspective.
    /// </summary>
    Strategic,
    
    /// <summary>
    /// Hypothesis-driven reasoning based on empirical evidence.
    /// </summary>
    Scientific
} 